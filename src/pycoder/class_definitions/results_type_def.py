"""Results type definitions for the AgentsCrowd platform.

This module defines the result types returned by agents after execution.
"""

from typing import Any, Dict, Optional
from pydantic import BaseModel


class AgentResult(BaseModel):
    """Model representing the result of an agent run.
    
    Attributes:
        content: The content generated by the agent.
        model: The model used to generate the content.
        timestamp: The timestamp when the content was generated.
        prompt: The prompt used to generate the content.
        metadata: Optional metadata for the result.
    """
    
    content: str
    model: str
    timestamp: float
    prompt: str
    metadata: Optional[Dict[str, Any]] = None
